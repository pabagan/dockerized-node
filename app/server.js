'use strict';

const express = require('express');

// Constants
const PORT = 8080;
const HOST = '0.0.0.0';

// App
const app = express();
app.get('/', (req, res) => {
  res.send('Hello world\n');
});

app.listen(PORT, HOST);
console.log(`Running on container http://${HOST}:${PORT}`);
console.log(`Running on host on http://${HOST}:${process.env.APP_PORT}`);